public class datosfile{
    private int diafile;
    private int mesfile;
    private int yearfile;

    public int getDiafile() {
        return diafile;
    }

    public void setDiafile(int diafile) {
        this.diafile = diafile;
    }

    public int getMesfile() {
        return mesfile;
    }

    public void setMesfile(int mesfile) {
        this.mesfile = mesfile;
    }

    public int getYearfile() {
        return yearfile;
    }
    
    public void setYearfile(int yearfile) {
        this.yearfile = yearfile;
    }

}  